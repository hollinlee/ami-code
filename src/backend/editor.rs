fn state_root_from_environment() -> Result<PathBuf> {
    if let Some(path) = env::var_os("AMI_CODE_STATE_DIR") {
        return Ok(PathBuf::from(path));
    }
    if let Some(path) = env::var_os("XDG_STATE_HOME") {
        return Ok(PathBuf::from(path).join("ami-code"));
    }
    let home = env::var_os("HOME")
        .map(PathBuf::from)
        .context("no state directory: set AMI_CODE_STATE_DIR, XDG_STATE_HOME, or HOME")?;
    #[cfg(target_os = "macos")]
    return Ok(home.join("Library/Application Support/ami-code"));
    #[cfg(not(target_os = "macos"))]
    Ok(home.join(".local/state/ami-code"))
}

fn remove_endpoint(path: &Path) -> Result<()> {
    match fs::remove_file(path) {
        Ok(()) => Ok(()),
        Err(error) if error.kind() == io::ErrorKind::NotFound => Ok(()),
        Err(error) => {
            Err(error).with_context(|| format!("failed to remove Nvim endpoint {}", path.display()))
        }
    }
}

fn workspace_safe_path(root: &Path, requested: &Path) -> Result<PathBuf, NvimRemoteError> {
    let candidate = normalize_absolute(if requested.is_absolute() {
        requested.to_path_buf()
    } else {
        root.join(requested)
    });
    let mut missing = Vec::new();
    let mut existing = candidate.as_path();
    while !existing.exists() {
        let name = existing.file_name().ok_or_else(|| {
            NvimRemoteError::InvalidPath(anyhow::anyhow!("path has no existing ancestor"))
        })?;
        missing.push(name.to_os_string());
        existing = existing
            .parent()
            .ok_or_else(|| NvimRemoteError::InvalidPath(anyhow::anyhow!("path has no parent")))?;
    }
    let root = root
        .canonicalize()
        .map_err(|error| NvimRemoteError::InvalidPath(error.into()))?;
    let mut resolved = existing
        .canonicalize()
        .map_err(|error| NvimRemoteError::InvalidPath(error.into()))?;
    if !resolved.starts_with(&root) {
        return Err(NvimRemoteError::OutsideWorkspace(candidate));
    }
    for component in missing.into_iter().rev() {
        resolved.push(component);
    }
    Ok(resolved)
}

fn workspace_existing_regular_file(
    root: &Path,
    requested: &Path,
) -> Result<PathBuf, NvimRemoteError> {
    let candidate = if requested.is_absolute() {
        requested.to_path_buf()
    } else {
        root.join(requested)
    };
    let root = root
        .canonicalize()
        .map_err(|error| NvimRemoteError::InvalidPath(error.into()))?;
    let resolved = candidate
        .canonicalize()
        .map_err(|error| NvimRemoteError::InvalidPath(error.into()))?;
    if !resolved.starts_with(&root) {
        return Err(NvimRemoteError::OutsideWorkspace(candidate));
    }
    let metadata =
        fs::metadata(&resolved).map_err(|error| NvimRemoteError::InvalidPath(error.into()))?;
    if !metadata.is_file() {
        return Err(NvimRemoteError::NotRegularFile(candidate));
    }
    Ok(resolved)
}

fn normalize_absolute(path: PathBuf) -> PathBuf {
    let mut normalized = PathBuf::new();
    for component in path.components() {
        match component {
            Component::Prefix(prefix) => normalized.push(prefix.as_os_str()),
            Component::RootDir => normalized.push(component.as_os_str()),
            Component::CurDir => {}
            Component::ParentDir => {
                normalized.pop();
            }
            Component::Normal(value) => normalized.push(value),
        }
    }
    normalized
}

fn absolute_path(path: &Path) -> Result<PathBuf> {
    if path.is_absolute() {
        Ok(path.to_path_buf())
    } else {
        Ok(env::current_dir()?.join(path))
    }
}

fn create_private_dir_all(path: &Path) -> Result<()> {
    fs::create_dir_all(path)?;
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        fs::set_permissions(path, fs::Permissions::from_mode(0o700))?;
    }
    Ok(())
}

fn atomic_replace(path: &Path, contents: &[u8]) -> Result<()> {
    let parent = path.parent().context("managed asset has no parent")?;
    let temporary = parent.join(format!(
        ".init.lua.tmp-{}-{}",
        std::process::id(),
        TEMP_SEQUENCE.fetch_add(1, Ordering::Relaxed)
    ));
    let mut options = OpenOptions::new();
    options.write(true).create_new(true);
    #[cfg(unix)]
    {
        use std::os::unix::fs::OpenOptionsExt;
        options.mode(0o600);
    }
    let result = (|| -> Result<()> {
        let mut file = options.open(&temporary)?;
        file.write_all(contents)?;
        file.sync_all()?;
        fs::rename(&temporary, path)?;
        #[cfg(unix)]
        {
            use std::os::unix::fs::PermissionsExt;
            fs::set_permissions(path, fs::Permissions::from_mode(0o600))?;
        }
        Ok(())
    })();
    if result.is_err() {
        let _ = fs::remove_file(&temporary);
    }
    result
}

