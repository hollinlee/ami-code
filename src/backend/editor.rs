/// Materialized, versioned configuration for the isolated managed Nvim profile.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ManagedNvimProfile {
    init_path: PathBuf,
}

impl ManagedNvimProfile {
    pub fn from_environment() -> Result<Self> {
        Self::materialize(state_root_from_environment()?)
    }

    pub fn materialize(state_root: impl AsRef<Path>) -> Result<Self> {
        let directory = state_root.as_ref().join("nvim").join(PROFILE_VERSION);
        create_private_dir_all(&directory).with_context(|| {
            format!(
                "failed to create managed Nvim profile at {}",
                directory.display()
            )
        })?;
        let init_path = directory.join("init.lua");
        atomic_replace(&init_path, INIT_LUA.as_bytes()).with_context(|| {
            format!(
                "failed to materialize managed Nvim init at {}",
                init_path.display()
            )
        })?;
        Ok(Self { init_path })
    }

    pub fn init_path(&self) -> &Path {
        &self.init_path
    }

    pub fn generation(&self, workspace: &Workspace) -> Result<ManagedNvimGeneration> {
        ManagedNvimGeneration::new(self, workspace)
    }
}

/// One Nvim process generation and its unique, short-lived listen endpoint.
#[derive(Debug)]
pub struct ManagedNvimGeneration {
    spec: ProcessSpec,
    endpoint: PathBuf,
    _runtime_dir: tempfile::TempDir,
}

#[allow(dead_code)]
impl ManagedNvimGeneration {
    fn new(profile: &ManagedNvimProfile, workspace: &Workspace) -> Result<Self> {
        let runtime_dir = tempfile::Builder::new()
            .prefix("ami-nv-")
            .tempdir()
            .context("failed to create private Nvim runtime directory")?;
        #[cfg(unix)]
        {
            use std::os::unix::fs::PermissionsExt;
            fs::set_permissions(runtime_dir.path(), fs::Permissions::from_mode(0o700))
                .context("failed to secure private Nvim runtime directory")?;
        }
        let endpoint = runtime_dir.path().join("nvim.sock");
        let init_path = profile
            .init_path()
            .to_str()
            .context("managed Nvim init path is not valid UTF-8")?;
        let endpoint_arg = endpoint
            .to_str()
            .context("managed Nvim endpoint path is not valid UTF-8")?;
        let mut spec = build_backend_process_spec("nvim", "nvim", workspace);
        spec.args = vec![
            "-u".into(),
            init_path.to_owned(),
            "--noplugin".into(),
            "-i".into(),
            "NONE".into(),
            "--listen".into(),
            endpoint_arg.to_owned(),
        ];
        spec.env.insert("NVIM_APPNAME".into(), APP_NAME.into());
        // Overlay inheritance is deliberately opt-in. If present, normalize it
        // to an absolute path; otherwise the managed init has no user hook.
        if let Some(overlay) = env::var_os(OVERLAY_ENV) {
            let overlay = absolute_path(Path::new(&overlay))?;
            let overlay = overlay
                .to_str()
                .context("managed Nvim overlay path is not valid UTF-8")?;
            spec.env.insert(OVERLAY_ENV.into(), overlay.to_owned());
        }
        Ok(Self {
            spec,
            endpoint,
            _runtime_dir: runtime_dir,
        })
    }

    pub fn process_spec(&self) -> &ProcessSpec {
        &self.spec
    }

    pub fn endpoint(&self) -> &Path {
        &self.endpoint
    }

    pub fn controller(&self, workspace: &Workspace) -> NvimController {
        NvimController::new(workspace.root().to_path_buf(), Some(self.endpoint.clone()))
    }

    pub fn cleanup(&self) -> Result<()> {
        remove_endpoint(&self.endpoint)
    }
}

impl Drop for ManagedNvimGeneration {
    fn drop(&mut self) {
        let _ = remove_endpoint(&self.endpoint);
    }
}

#[derive(Debug, Clone)]
pub struct NvimController {
    workspace_root: PathBuf,
    endpoint: Option<PathBuf>,
}

#[allow(dead_code)]
#[derive(Debug, Error)]
pub enum NvimRemoteError {
    #[error("Nvim has no active endpoint")]
    NoEndpoint,
    #[error("Nvim endpoint is not ready: {0}")]
    NotReady(PathBuf),
    #[error("path is outside the workspace: {0}")]
    OutsideWorkspace(PathBuf),
    #[error("path is not valid UTF-8 and cannot be passed to Nvim: {0}")]
    NonUtf8Path(PathBuf),
    #[error("failed to resolve remote-open path: {0}")]
    InvalidPath(#[source] anyhow::Error),
    #[error("path is not an existing regular file: {0}")]
    NotRegularFile(PathBuf),
    #[error("failed to execute Nvim remote command: {0}")]
    Command(#[source] io::Error),
    #[error("Nvim remote command failed with {0}")]
    RemoteFailure(std::process::ExitStatus),
}

#[allow(dead_code)]
impl NvimController {
    pub fn new(workspace_root: PathBuf, endpoint: Option<PathBuf>) -> Self {
        Self {
            workspace_root,
            endpoint,
        }
    }

    pub fn endpoint(&self) -> Option<&Path> {
        self.endpoint.as_deref()
    }

    pub fn replace_endpoint(&mut self, endpoint: Option<PathBuf>) {
        self.endpoint = endpoint;
    }

    pub fn remote_open_spec(&self, path: impl AsRef<Path>) -> Result<ProcessSpec, NvimRemoteError> {
        let absolute = workspace_safe_path(&self.workspace_root, path.as_ref())?;
        self.remote_open_spec_for_absolute(absolute)
    }

    /// Builds a remote-open command only for an existing regular file whose
    /// canonical target is inside the canonical workspace root. Symlink files
    /// are accepted; escaping symlinks and every non-regular file are rejected.
    pub fn existing_file_open_spec(
        &self,
        path: impl AsRef<Path>,
    ) -> Result<ProcessSpec, NvimRemoteError> {
        let absolute = workspace_existing_regular_file(&self.workspace_root, path.as_ref())?;
        self.remote_open_spec_for_absolute(absolute)
    }

    fn remote_open_spec_for_absolute(
        &self,
        absolute: PathBuf,
    ) -> Result<ProcessSpec, NvimRemoteError> {
        let endpoint = self.endpoint.clone().ok_or(NvimRemoteError::NoEndpoint)?;
        if !endpoint.exists() {
            return Err(NvimRemoteError::NotReady(endpoint));
        }
        let endpoint_arg = endpoint
            .to_str()
            .ok_or_else(|| NvimRemoteError::NonUtf8Path(endpoint.clone()))?;
        let absolute_arg = absolute
            .to_str()
            .ok_or_else(|| NvimRemoteError::NonUtf8Path(absolute.clone()))?;
        let mut spec = ProcessSpec::new("nvim").display_name("nvim remote");
        spec.args = vec![
            "--server".into(),
            endpoint_arg.to_owned(),
            "--remote".into(),
            absolute_arg.to_owned(),
        ];
        spec.cwd = Some(self.workspace_root.clone());
        spec.env.insert("NVIM_APPNAME".into(), APP_NAME.into());
        Ok(spec)
    }

    /// Executes a one-shot argv-based remote open. No path is interpreted by a shell.
    pub fn open(&self, path: impl AsRef<Path>) -> Result<(), NvimRemoteError> {
        self.execute(self.remote_open_spec(path)?)
    }

    /// Executes the strict existing-regular-file variant used by Sidebar.
    pub fn open_existing_file(&self, path: impl AsRef<Path>) -> Result<(), NvimRemoteError> {
        self.execute(self.existing_file_open_spec(path)?)
    }

    fn execute(&self, spec: ProcessSpec) -> Result<(), NvimRemoteError> {
        execute_remote_spec(spec)
    }
}

fn execute_remote_spec(spec: ProcessSpec) -> Result<(), NvimRemoteError> {
    let cwd = spec.cwd.as_ref().ok_or_else(|| {
        NvimRemoteError::InvalidPath(anyhow::anyhow!("Nvim controller has no workspace cwd"))
    })?;
    let mut command = Command::new(&spec.program);
    command
        .args(&spec.args)
        .current_dir(cwd)
        // A one-shot `nvim --server --remote` detects inherited raw TTYs and
        // enters/leaves the alternate screen, corrupting the parent ratatui
        // display. It does not need interactive streams.
        .stdin(Stdio::null())
        .stdout(Stdio::null())
        .stderr(Stdio::null());
    for (key, value) in &spec.env {
        command.env(key, value);
    }
    let status = command.status().map_err(NvimRemoteError::Command)?;
    if !status.success() {
        return Err(NvimRemoteError::RemoteFailure(status));
    }
    Ok(())
}

